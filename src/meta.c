#define META_CLASS_DESCRIPTIONS_COUNT 978

typedef int (*serializeFunction)(FILE *stream, struct TreeNode *node, uint32_t flags);

struct MetaClassDescription
{
    uint8_t *name;

    serializeFunction read;
    serializeFunction write;
};

static struct MetaClassDescription metaClassDescriptions[META_CLASS_DESCRIPTIONS_COUNT] = {{0}};

void readMetaStreamHeader(FILE *stream, struct MetaStreamHeader *header)
{
    fread(&(header->version), sizeof(header->version), 1, stream);
    fread(&(header->defaultSize), sizeof(header->defaultSize), 1, stream);
    fread(&(header->debugSize), sizeof(header->debugSize), 1, stream);
    fread(&(header->asyncSize), sizeof(header->asyncSize), 1, stream);
    fread(&(header->numVersion), sizeof(header->numVersion), 1, stream);
    header->crc = malloc((sizeof(struct MetaStreamHeader)) * header->numVersion);

    for (uint32_t i = 0; i < header->numVersion; ++i)
    {
        fread(header->crc + i, sizeof(header->crc->typeSymbolCrc) + sizeof(header->crc->versionCrc), 1, stream);
    }
}

void readMetaStream(FILE *stream, struct MetaStreamHeader *header)
{
    readMetaStreamHeader(stream, header);

    if ((int32_t)header->defaultSize < 0)
    {
        uint32_t defaultStart = (uint32_t)cftell(stream);
        streamDecrypt(&stream);
        header->defaultSize = (uint32_t)(cftell(stream) - defaultStart);
    }
    else
    {
        cfseek(stream, header->defaultSize, SEEK_CUR);
    }

    if ((int32_t)header->debugSize < 0)
    {
        uint32_t debugStart = (uint32_t)cftell(stream);
        streamDecrypt(&stream);
        header->debugSize = (uint32_t)(cftell(stream) - debugStart);
    }
    else
    {
        cfseek(stream, header->debugSize, SEEK_CUR);
    }

    if ((int32_t)header->asyncSize < 0)
    {
        uint32_t asyncStart = (uint32_t)cftell(stream);
        streamDecrypt(&stream);
        header->asyncSize = (uint32_t)(cftell(stream) - asyncStart);
    }
    else
    {
        cfseek(stream, header->asyncSize, SEEK_CUR);
    }

    cfseek(stream, (-((int64_t)header->defaultSize + (int64_t)header->debugSize + (int64_t)header->asyncSize)), SEEK_CUR); // Seeks back to the end of header
}

void writeMetaStreamHeader(FILE *stream, struct MetaStreamHeader *header)
{
    fwrite(&(header->version), sizeof(header->version), 1, stream);
    fwrite(&(header->defaultSize), sizeof(header->defaultSize), 1, stream);
    fwrite(&(header->debugSize), sizeof(header->debugSize), 1, stream);
    fwrite(&(header->asyncSize), sizeof(header->asyncSize), 1, stream);
    fwrite(&(header->numVersion), sizeof(header->numVersion), 1, stream);

    for (uint32_t i = 0; i < header->numVersion; ++i)
    {
        fwrite(header->crc + i, sizeof(header->crc->typeSymbolCrc) + sizeof(header->crc->versionCrc), 1, stream);
    }
}

int readMetaClass(FILE *stream, struct TreeNode *node, uint32_t flags)
{
    enum Type type = searchDatabase("protonDatabase.db", node->typeSymbol);
    // printf("Call %s\n", metaClassDescriptions[type].name);
    serializeFunction readFunction = metaClassDescriptions[type].read;
    if (readFunction == NULL)
    {
        printf("Error: Read function not implemented for %s type = %" PRIx64 "\n", metaClassDescriptions[type].name, node->typeSymbol);
        return -1;
    }
    return metaClassDescriptions[type].read(stream, node, flags);
}

int writeMetaClass(FILE *stream, struct TreeNode *node, uint32_t flags)
{
    enum Type type = searchDatabase("protonDatabase.db", node->typeSymbol);
    serializeFunction writeFunction = metaClassDescriptions[type].write;
    if (writeFunction == NULL)
    {
        printf("Error: Write function not implemented for %s, type = %" PRIx64 "\n", metaClassDescriptions[type].name, node->typeSymbol);
        return -1;
    }
    return metaClassDescriptions[type].write(stream, node, flags);
}

char *getMetaClassName(uint64_t typeSymbol)
{
    return metaClassDescriptions[searchDatabase("protonDatabase.db", typeSymbol)].name;
}

